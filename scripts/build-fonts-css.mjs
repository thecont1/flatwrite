// scripts/build-fonts-css.mjs
//
// Regenerate public/fonts.css from core/font-inventory.js. The static
// index.html page links to fonts.css via <link rel="stylesheet">; the
// render path (core/font-loader.js) imports the same inventory directly
// and embeds the woff2 as a data URI.
//
// Run after adding/removing a font:
//   node scripts/build-fonts-css.mjs

import { writeFileSync, readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..');

// CommonJS inventory import — Node's ESM/CJS interop is straightforward here.
const { FONT_INVENTORY, UNICODE_RANGE } = await import(
  'file://' + resolve(repoRoot, 'core/font-inventory.js')
);

function blockFor(family, face) {
  return `@font-face {
  font-family: '${family}';
  font-style: ${face.style};
  font-weight: ${face.weight};
  font-display: swap;
  src: url('fonts/${face.file}') format('woff2');
  unicode-range: ${UNICODE_RANGE};
}`;
}

const out = [];
out.push('/* Auto-generated by scripts/build-fonts-css.mjs from core/font-inventory.js. */');
out.push('/* Do not edit by hand — run the generator instead. */');
out.push('');

for (const [family, faces] of Object.entries(FONT_INVENTORY)) {
  for (const face of faces) {
    out.push(`/* ${family} ${face.weight === 'normal' ? '' : `(weight ${face.weight})`} */`);
    out.push(blockFor(family, face));
    out.push('');
  }
}

const target = resolve(repoRoot, 'public', 'fonts.css');
writeFileSync(target, out.join('\n'), 'utf8');
console.log(`wrote ${target} (${out.join('\n').length} bytes)`);